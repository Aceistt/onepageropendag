<section id="vakken"> 
    <h2>Vakken</h2>
    <ul>
        <li>
            <div class="card">
                <h3>PRA (Praktijk)</h3>
                <p>In PRA werk je zelfstandig aan opdrachten die school heeft verzonnen. Je gebruikt hierbij alle kennis uit de theorielessen. Soms werk je alleen, vaak in groepjes. Zo leer je goed samen te werken.</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>PRO (Praktijk Ondersteuning)</h3>
                <p>PRO leert je plannen en ontwerpen voordat je begint met het maken van een project (code schrijven).</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>NATIVE</h3>
                <p>In blok A werk je met de micro:bit en programmeer je met blokjes. Daarna ga je aan de slag met je eerste programmeertaal: Python, waar je leert werken met loops en bestanden inlezen.</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>WEB</h3>
                <p>Je leert websites maken met HTML en CSS. Aan het einde van het blok leer je ook een beetje PHP en JavaScript. In blok B leer je PHP en het koppelen van een database.</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>Nederlands</h3>
                <p>In blok A maak je opdrachten in Taalblokken en een paar toetsen. In latere blokken werk je aan je stagebrief en CV.</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>Engels</h3>
                <p>In blok A maak je opdrachten en toetsen. Als je snel doorwerkt, kun je Engels in het eerste jaar afronden, zodat je de volgende jaren hier geen lessen meer voor hoeft te volgen.</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>Rekenen</h3>
                <p>In blok A bestaan de lessen uit opdrachten en toetsen, net als bij Engels en Nederlands. Soms kan er een toets worden gevraagd om te kijken of je klaar bent voor het examen. Bij voldoende resultaat hoef je het examen niet meer te maken.</p>
            </div>
        </li>
        <li>
            <div class="card">
                <h3>Mentorles</h3>
                <p>In blok A kijk je naar je toekomst en hoe je over een paar jaar na de opleiding staat. In volgende blokken wordt deze les gebruikt voor ondersteuning bij je stage. Je hebt ook gespreksmomenten om te evalueren wat goed gaat en waar je nog aan moet werken.</p>
            </div>
        </li>
    </ul>
</section>



