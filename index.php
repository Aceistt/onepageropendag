    <h2>Over Ons</h2>
    <p>Wij zijn Curio, een onderwijsinstelling die zich richt op het bieden van kwalitatief hoogstaand beroepsonderwijs. Onze missie is om studenten niet alleen de kennis en vaardigheden te geven die ze nodig hebben voor hun toekomstige carrière, maar ook om hen te inspireren en te begeleiden in hun persoonlijke ontwikkeling.</p>
    <p>Bij Curio geloven we in leren door te doen. Onze opleidingen zijn praktijkgericht en sluiten nauw aan bij de behoeften van het bedrijfsleven. Dit betekent dat je tijdens je studie niet alleen theorie leert, maar ook direct aan de slag gaat met echte projecten en opdrachten. Zo ben je goed voorbereid op de arbeidsmarkt.</p>
    <p>Wat ons uniek maakt, is onze persoonlijke aanpak. Bij Curio sta jij als student centraal. Onze docenten en begeleiders nemen de tijd om je te leren kennen en je te ondersteunen waar nodig. Of je nu hulp nodig hebt bij je studie, je persoonlijke ontwikkeling of het vinden van een stageplek, wij staan voor je klaar.</p>
    <p>Tijdens de open dag krijg je de kans om onze opleidingen, docenten en studenten te leren kennen. Ontdek de sfeer, stel al je vragen en ontdek of Curio de plek is waar jij je thuis voelt en je toekomst kunt opbouwen.</p>
  </section>

